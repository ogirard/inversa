<h1>Panflötenunterricht / Standorte</h1>

Die Lehrpersonen, Nicole Andris und Hanspeter Oggier, sind Panflötisten mit abgeschlossenem Berufsstudium und Konzertdiplom.
Wir sind beide Mitglieder im Musiklehrerverband SMPV.<br/>
Unser Unterricht steht allen Altersgruppen offen (Kinder in der Regel ab 8 Jahren). Der Unterricht findet in Einzel- oder Gruppenlektionen statt.
Wir stellen den Anspruch, uns stetig in pädagogischer wie auch in musikalischer Richtung weiterzubilden. Das Unterrichtsmaterial wird individuell für
den Schüler zusammengestellt und angefertigt.

Willkommen sind Anfänger, Fortgeschritten und Berufsstudenten.
