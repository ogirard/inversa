<h1>Panflöten Akademie Syrinx</h1>
<p>

Die Syrinx Panflöten sind ursprünglich in Zusammenarbeit mit Simion Stanciu Syrinx und nach seinen Vorstellungen gefertigt worden. Sie werden heute vom Panflötenbauer Steffen Küttner in Handarbeit gebaut.
Jede Syrinx Panflöte kann vor dem Kauf gemietet werden. Die Miete wird dem Kaufpreis angerechnet (maximal ein Jahr Miete).
<br />
<h2>Die Flöten zeichnen sich aus durch:</h2>
<span class="option">—</span> Leichtigkeit und gute Handhabung<br />
<span class="option">—</span> Stabile Intonation<br />
<span class="option">—</span> Präzise Ansprache<br />
<span class="option">—</span> Möglichkeit differenzierter Klanggestaltung<br />
<span class="option">—</span> Ein breites dynamisches Spektrum<br />
<span class="option">—</span> Orientierungspunkte auf der Innenseite der Rohre<br />
<br />
Im Panflötenbau sind wir darauf bedacht, neue Erkenntnisse mit einzubauen und so die Entwicklung voranzutreiben.<br />
